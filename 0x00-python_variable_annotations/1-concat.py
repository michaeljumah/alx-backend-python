#!/usr/bin/env python3

""" takes a string str1 and a string str2 as arguments and returns a concatenated string"""


def concat(str1: str, str2: str) -> str:
    """ Type-annotated function concat that takes a string arguments.
        Args:
            str1: str type.
            str2: str type.
        Return:
            concatinated string.
    """
    return str1 + str2
